export function t(key: TranslationKeys, locale: Locale = 'en'): string {
  const val = translations[locale]?.[key];
  if (val) return val;
  // fallback to English
  const enVal = translations.en[key];
  if (enVal) return enVal;
  return key;
}

export function isRTL(locale: Locale): boolean {
  return RTL_LOCALES.includes(locale);
}

export function getPhotoLabel(key: string, locale: Locale): string {
  const mapping: Record<string, TranslationKeys> = {
    front: 'photo.front',
    passenger_side: 'photo.passenger_side',
    back: 'photo.back',
    driver_side: 'photo.driver_side',
    interior: 'photo.interior',
  };
  const tKey = mapping[key];
  if (tKey) return t(tKey, locale);
  return key;
}
